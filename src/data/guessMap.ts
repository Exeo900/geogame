const difficultyMap = new Map<string, Difficulty>([
  ...easyCodes.map((c): [string, Difficulty] => [c, 'easy']),
  ...mediumCodes.map((c): [string, Difficulty] => [c, 'medium']),
  ...hardCodes.map((c): [string, Difficulty] => [c, 'hard']),
])

export const mapCountries: MapCountry[] = countries
  .filter((c) => difficultyMap.has(c.code) && numericCodes[c.code] && geoData[c.code])
  .map((c) => ({
    ...c,
    numericCode: numericCodes[c.code],
    difficulty: difficultyMap.get(c.code)!,
    center: geoData[c.code].center,
    area: geoData[c.code].area,
  }))

export function getByDifficulty(difficulty: Difficulty): MapCountry[] {
  return mapCountries.filter((c) => c.difficulty === difficulty)
}
